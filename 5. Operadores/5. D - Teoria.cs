using System;


namespace Atribuicao
{ 

	public class Teoria
	{
		public Teoria()
		{

            // =: Atribuição simples. Atribui o valor do lado direito ao operando do lado esquerdo.
            // +=: Atribuição com adição.Adiciona o valor do lado direito ao operando do lado esquerdo e atribui o resultado ao operando do lado esquerdo.
            // -=: Atribuição com subtração.Subtrai o valor do lado direito do operando do lado esquerdo e atribui o resultado ao operando do lado esquerdo.
            // *=: Atribuição com multiplicação.Multiplica o valor do lado direito ao operando do lado esquerdo e atribui o resultado ao operando do lado esquerdo.
            // /=: Atribuição com divisão.Divide o valor do lado esquerdo pelo valor do lado direito e atribui o resultado ao operando do lado esquerdo.
            // %=: Atribuição com módulo.Divide o valor do lado esquerdo pelo valor do lado direito e atribui o resto(módulo) da divisão ao operando do lado esquerdo.
            // <<=: Atribuição com deslocamento à esquerda.Desloca os bits do operando do lado esquerdo para a esquerda pelo número de posições especificado pelo operando do lado direito e atribui o resultado ao operando do lado esquerdo.
            // >>=: Atribuição com deslocamento à direita.Desloca os bits do operando do lado esquerdo para a direita pelo número de posições especificado pelo operando do lado direito e atribui o resultado ao operando do lado esquerdo.
            // &=: Atribuição com operação AND bit a bit.Realiza uma operação AND bit a bit entre o operando do lado esquerdo e o operando do lado direito e atribui o resultado ao operando do lado esquerdo.
            // |=: Atribuição com operação OR bit a bit.Realiza uma operação OR bit a bit entre o operando do lado esquerdo e o operando do lado direito e atribui o resultado ao operando do lado esquerdo.
            // ^=: Atribuição com operação XOR bit a bit.Realiza uma operação XOR bit a bit entre o operando do lado esquerdo e o operando do lado direito e atribui o resultado ao operando do lado esquerdo.
            // x++ : operador de pós-incremento, soma mais 1 a variavel atribuida
            // x-- : operador de pós-decremento, diminuir menos 1 a variavel atribuida
            // ++x : operador de pré-incremento, soma mais 1 a variavel atribuida
            // --x : operador de pré-decremento, diminuir menos 1 a variavel atribuida


        }
    }

}
